package day01_Variables;

public class C03_Variables {
    public static void main(String[] args) {

        int sayi1= 10;
        int sayi2= 20;

        String sayi3="10";
        String sayi4="20";

        System.out.println(sayi1+sayi2); // 30
        System.out.println(sayi3+sayi4); //1020
        System.out.println("sayi:"+sayi3+sayi4);  //sayi:1020


    }
}
